    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         int r = jTable1.getSelectedRow();
        if(r> -1){
            listS.remove(r);
            if(jTextField1.getText().length() != 0 && jTextField2.getText().length() != 0 && jTextField3.getText().length() != 0){
                int id = 10000;
                while(true){
                    if(OK1(id)) break;
                    id++;
                }
                SinhVIen s = new SinhVIen();
                s.setID(id);
                s.setTen(jTextField1.getText());
                s.setDiachi(jTextField2.getText());
                s.setLop(jTextField3.getText());

                if(r< listS.size()){
                    listS.add(r,s);
                }
                else listS.add(s);

                file.ghiSV(listS);
                H1();
            } else JOptionPane.showMessageDialog(rootPane, "Nhap day du thong tin!");
        }else JOptionPane.showMessageDialog(rootPane, "Chon mot hang de sua roi hay nhan luu!");
    }//GEN-LAST:event_jButton3ActionPerformed

