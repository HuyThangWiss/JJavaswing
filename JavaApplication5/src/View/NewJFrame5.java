    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        Collections.sort(listS, new Comparator<SinhVIen>() {
                @Override
                public int compare(SinhVIen o1, SinhVIen o2) {
                    return o1.getTen().substring(o1.getTen().lastIndexOf(" "))
                    .compareTo(o2.getTen().substring(o2.getTen().lastIndexOf(" ")));
                }
            });
    }//GEN-LAST:event_jButton5ActionPerformed
  public boolean OK1(int id){
        for(SinhVIen s: listS)
            if(s.getID() == id) return false;
        return true;
    }
